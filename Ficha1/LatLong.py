import re

Coordenadas = [    
'(75, 180)',
'(+90.0, -147.45)',
'(77.11112223331, 149.99999999)',
'(+90, +180)',
'(90, 180)',
'(-90.00000, -180.0000)',
'(75, 280)',
'(+190.0, -147.45)',
'(77.11112223331, 249.99999999)',
'(+90, +180.2)',
'(90., 180.)',
'(-090.00000, -180.0000)'
]

coordp = r'^\((\+|-)?([1-8]\d(\.\d+)?|90(\.0+)?|\d(\.\d+)?), (\+|-)?(1[0-7]\d(\.\d+)?|[1-9]?\d(\.\d+)?|180(\.0+)?)\)$'

for s in Coordenadas:
    mo = re.search(coordp,s)
    if mo:
        print('Match: ',s)
    else :
        print('No match: ',s)